

//! # Rust 动态数组示例程序
//!
//! 本程序演示了 Rust 中 Vec（动态数组）的基本用法，包括：
//! - 创建空的动态数组
//! - 向数组中添加元素
//! - 类型推导和显式类型声明
//! - 元素截断行为

use std::fmt::Debug;

/// 主函数：演示 Vec 的基本操作
fn main() {
    // 创建一个 u8 类型的元素，值为 5
    // u8 是无符号 8 位整数，范围是 0 到 255
    let elem = 5u8;
    
    // 创建一个空的动态数组（Vec）
    // 使用 Vec::new() 方法创建一个空的 Vec，类型推导为 Vec<i32>
    // 如果需要其他类型，可以使用 Vec::<T>::new() 或 Vec::with_capacity(n)
    let mut vec = Vec::new();

    // 向动态数组中添加元素
    // push 方法会将元素添加到数组的末尾
    // 注意：elem 是 u8 类型，但 vec 推导为 Vec<i32>，会发生类型转换
    vec.push(elem);

    // 打印动态数组的内容
    // 使用 {:?} 格式化器来显示 Debug trait 的实现
    // 输出结果：[5]，表示包含一个元素 5 的数组
    println!("{:?}", vec);
}