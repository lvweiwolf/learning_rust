

/// `let v = vec![0, 1];` 的意思是：使用 `vec!` 宏创建一个 `Vec`（动态数组）并用给定元素初始化它。
///
/// - `vec![0, 1]` 会生成一个包含两个元素 `0` 和 `1` 的向量（`Vec`）
/// - `let v = ...` 将这个向量绑定到变量 `v` 上，变量名为 `v`
///
/// 等价写法示例：
/// - `let v = Vec::from([0, 1]);`
/// - `let v = vec![0, 1];`（最常用）
///
/// 类型推导：在没有额外上下文时，`v` 通常会被推导为 `Vec<i32>`；也可以显式写为：
/// - `let v: Vec<u8> = vec![0, 1];`
/// - `vec![0, 1]` 会生成一个包含两个元素 `0` 和 `1` 的 `Vec`
/// - `let v = ...` 将这个向量绑定到变量 `v` 上
///
/// 结果：`v` 的值等价于 `Vec::from([0, 1])`，类型通常会被推导为 `Vec<i32>`（若无其他上下文约束）。
fn main() {
    let elem = 5u8;
    let mut vec = Vec::new();

    vec.push(elem);

    println!("{:?}", vec);
}